package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {
		//Print number from 1 to 10
		//Starting number is 1
		//Ending number is 10
		//increment by 1 (++)
		
		// Loop should execute when value of i is 1 to 5
		
		for(int i=1;i<=5;i++) {
			System.out.println(i);
		}
		
		
		System.out.println("---------------");
		//Executing from highest to lowest - Reverse For loop
		//Starting from 5 to 1
		                           
		for(int i=5;i>=1;i--) {
			System.out.println(i);
		}

	}

}
