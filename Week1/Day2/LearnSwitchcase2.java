package week1.day2;

public class LearnSwitchcase2 {

	public static void main(String[] args) {
		int number=1;
		
		switch(number) {
		case 0:
			System.out.println("The number is Zero");
			break;
		case 1:
			System.out.println("The number is greater than Zero");
		    break;
		}
  }

}
